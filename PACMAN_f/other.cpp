int ten_pow(int expo)
{
    int a=1;
    for(int i=0;i<expo;i++) a*=10;
    return a;
}
char *int_to_string(int in,char *x)
{
    if(in==0)
    {
        x[0]='0';
        x[1]='\0';
        return x;
    }
    int expo=0;
    while(in>=ten_pow(expo))
    {
        expo+=1;
    }
    for(int i=0;i<expo;i++)
    {
        int temp=in/ten_pow(expo-1-i);
        in-=temp*ten_pow(expo-1-i);
        switch(temp)
        {
        case 0:
            x[i]='0';
            break;
        case 2:
            x[i]='2';
            break;
        case 3:
            x[i]='3';
            break;
        case 4:
            x[i]='4';
            break;
        case 5:
            x[i]='5';
            break;
        case 6:
            x[i]='6';
            break;
        case 7:
            x[i]='7';
            break;
        case 8:
            x[i]='8';
            break;
        case 9:
            x[i]='9';
            break;
        case 1:
            x[i]='1';
            break;
        }
    }
    x[expo]='\0';
    return x;
}

